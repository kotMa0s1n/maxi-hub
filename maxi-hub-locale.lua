function MaxiHubLocale.normalize(lang)
	if type(lang) == "string" and lang:lower() == "en" then
		return "en"
	end
	return "ru"
end

function MaxiHubLocale.t(lang, key)
	lang = MaxiHubLocale.normalize(lang)
	local bucket = MaxiHubLocale.TEXT[lang] or MaxiHubLocale.TEXT.ru
	return bucket[key] or MaxiHubLocale.TEXT.ru[key] or key
end

return MaxiHubLocale
